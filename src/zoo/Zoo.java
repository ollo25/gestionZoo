package zoo;

public class Zoo {
    private String name;
    private String adresse;
    private int nombreEnclos;
    private int heureOuverture;
    private int heureFermeture;


    public Zoo(String name, String adresse, int nombreEnclos, int heureOuverture, int heureFermeture) {
        this.name = name;
        this.adresse = adresse;
        this.nombreEnclos = nombreEnclos;
        this.heureOuverture = heureOuverture;
        this.heureFermeture = heureFermeture;
    }
}
